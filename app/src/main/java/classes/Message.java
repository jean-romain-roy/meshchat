package classes;

public class Message {

    // ---------------------------------------------------------------------------------------------
    // --------------------------------------- Attributes ------------------------------------------
    // ---------------------------------------------------------------------------------------------
    private String content_;
    private TimeStamp datetime_;
    private int author_;
    private int status_;


    // ---------------------------------------------------------------------------------------------
    // ------------------------------------- Constructors ------------------------------------------
    // ---------------------------------------------------------------------------------------------
    public Message(String content, int author){
        this.content_ = content;
        this.author_ = author;
        this.datetime_ = new TimeStamp();
        this.status_ = 1;
    }


    // ---------------------------------------------------------------------------------------------
    // ---------------------------------------- Getters --------------------------------------------
    // ---------------------------------------------------------------------------------------------
    public String getContent() {
        return content_;
    }

    public int getAuthor() {
        return author_;
    }

    public int getStatus() {
        return status_;
    }

    public TimeStamp getDatetime() {
        return datetime_;
    }
}
