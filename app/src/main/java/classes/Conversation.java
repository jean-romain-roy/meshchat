package classes;

import java.util.ArrayList;

public class Conversation {

    // ---------------------------------------------------------------------------------------------
    // --------------------------------------- Attributes ------------------------------------------
    // ---------------------------------------------------------------------------------------------
    private ArrayList<Message> messages_;
    private String correspondent_;


    // ---------------------------------------------------------------------------------------------
    // ------------------------------------- Constructors ------------------------------------------
    // ---------------------------------------------------------------------------------------------
    public Conversation(ArrayList<Message> messages, String correspondant){
        this.messages_ = messages;
        this.correspondent_ = correspondant;
    }


    // ---------------------------------------------------------------------------------------------
    // ---------------------------------------- Getters --------------------------------------------
    // ---------------------------------------------------------------------------------------------
    public ArrayList<Message> getMessages() {
        return messages_;
    }

    public String getCorrespondant() {
        return correspondent_;
    }
}
